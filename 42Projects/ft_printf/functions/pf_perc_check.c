#include	"../ft_printf.h"

int
	pf_perc_check(const char *(__), int x)
{
	if ((__)[x] == '+' || (__)[x] == '-' || (__)[x] == '.' || (__)[x] == '*'\
		|| (__)[x] == '#' || (__)[x] == ' '\
		|| (__)[x] == '0' || (__)[x] == '1'\
		|| (__)[x] == '2' || (__)[x] == '3'\
		|| (__)[x] == '4' || (__)[x] == '5'\
		|| (__)[x] == '6' || (__)[x] == '7'\
		|| (__)[x] == '8' || (__)[x] == '9'\
		|| (__)[x] == 'd' || (__)[x] == 'i'\
		|| (__)[x] == 'x' || (__)[x] == 'X'\
		|| (__)[x] == 'o' || (__)[x] == 'b'\
		|| (__)[x] == 'c' || (__)[x] == 's'\
		|| (__)[x] == 'p' || (__)[x] == 'u'\
		|| (__)[x] == 'f' || (__)[x] == 'n'\
		|| (__)[x] == '%')
		return ((signed int)(__)[x]);
	if ((__)[x] == 0)
		return (0);
	return (-1);
}

