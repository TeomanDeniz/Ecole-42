#include	"../../../ft_printf.h"

void
	plus(int *flags, char c)
{
	if (!(c == 'd' || c == 'i' || c == 'f') || !flags['+'] || flags[3])
		return ;
	write(1, "+", 1);
	flags[1] += 1;
}
