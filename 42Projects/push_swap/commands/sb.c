#include	"../push_swap.h"

void
	sb(struct s_stacks *stack)
{
	register int	temp;

	temp = stack->b[0];
	stack->b[0] = stack->b[1];
	stack->b[1] = temp;
	write(1, "sb\n", 3);
}
