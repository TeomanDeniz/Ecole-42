#include	"../../push_swap.h"

void
	short_a_three(struct s_stacks *s)
{
	if (1[s->a] < 2[s->a] && 0[s->a] < 2[s->a] && 0[s->a] > 1[s->a])
		sa(s);
	if (0[s->a] > 1[s->a] && 1[s->a] > 2[s->a] && 0[s->a] > 2[s->a])
	{
		sa(s);
		rra(s);
	}
	if (0[s->a] > 2[s->a] && 1[s->a] < 2[s->a] && 0[s->a] > 1[s->a])
		ra(s);
	if (1[s->a] > 2[s->a] && 0[s->a] < 1[s->a] && 0[s->a] < 2[s->a])
	{
		sa(s);
		ra(s);
	}
	if (0[s->a] > 2[s->a] && 1[s->a] > 2[s->a] && 0[s->a] < 1[s->a])
		rra(s);
}
